// Get all loading messages
const loadingMessages = document.querySelectorAll(".loading-text span");

// Function to show messages one by one
function showMessagesSequentially() {
  let index = 0;

  // Display messages at intervals
  const interval = setInterval(() => {
    // Hide the previous message
    if (index > 0) {
      loadingMessages[index - 1].classList.remove("active");
    }

    // Show the current message
    if (index < loadingMessages.length) {
      loadingMessages[index].classList.add("active");
      index++;
    } else {
      // Stop the interval once all messages are displayed
      clearInterval(interval);
    }
  }, 1000); // Change message every 1 second
}

// Hide loader and show content
function hideLoader() {
  const loader = document.getElementById("loader");
  const mainContent = document.getElementById("main-content");

  loader.style.display = "none"; // Hide the loader
  mainContent.style.display = "block"; // Show the main content
}

// Call the functions when the page loads
window.addEventListener("load", () => {
  showMessagesSequentially();

  // Hide loader after all animations complete (4 seconds)
  setTimeout(() => {
    hideLoader();
  }, 4000); // Matches the progress bar animation duration
});

